    private void reset() {
        jTextField1.setText("");
        jTextField1.setEnabled(true);
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField1.grabFocus();
        loadCustomers("first_name", "ASC", jTextField1.getText());
    }

    private void search() {
        int sort = jComboBox1.getSelectedIndex();

        switch (sort) {
            case 0:
                loadCustomers("first_name", "ASC", jTextField1.getText());
                break;
            case 1:
                loadCustomers("first_name", "DESC", jTextField1.getText());
                break;
            case 2:
                loadCustomers("point", "ASC", jTextField1.getText());
                break;
            case 3:
                loadCustomers("point", "DESC", jTextField1.getText());
                break;

        }
    }

    private void loadCustomers(String column, String orderby, String mobile) {
        try {
            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `mobile` LIKE  '" + mobile + "%' ORDER BY `" + column + "` " + orderby + "  ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();

                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("point"));

                dtm.addRow(vector);
            }

            resultSet.close();
        } catch (SQLException e) {
        }

    }

