    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String mobile = jTextField1.getText().trim();
        String fname = jTextField2.getText().trim();
        String lname = jTextField3.getText().trim();
        String email = jTextField4.getText().trim();

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter last name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            boolean canUpdate = false;

            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `email`='" + email + "'");

            try {
                if (resultSet.next()) {
                    if (resultSet.getString("mobile").equals(mobile)) {
                        canUpdate = true;
                        JOptionPane.showMessageDialog(this, "Customer details updated successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Email already added", "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                } else {
                    canUpdate = true;
                }
            } catch (SQLException ex) {
                Logger.getLogger(CustomerRegistrationForm.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (canUpdate) {
                MySQL2.executeIUD("UPDATE `customer` SET `first_name` = '" + fname + "', `last_name` = '" + lname + "', `email` = '" + email + "' WHERE `mobile` = '" + mobile + "' ");
                reset();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

