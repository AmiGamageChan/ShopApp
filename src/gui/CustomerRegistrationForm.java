    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String mobile = jTextField1.getText().trim();
        String fname = jTextField2.getText().trim();
        String lname = jTextField3.getText().trim();
        String email = jTextField4.getText().trim();

        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[01245678][0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter first name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter last name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '" + mobile + "' AND `email` = '" + email + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Customer is already registered", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL2.executeIUD("INSERT INTO `customer` (`mobile`, `first_name`, `last_name`, `email`, `point`)"
                            + " VALUES ('" + mobile + "', '" + fname + "', '" + lname + "', '" + email + "', 0)");

                    JOptionPane.showMessageDialog(this, "Customer registered successfully", "Information", JOptionPane.INFORMATION_MESSAGE);

                    reset();
                }

                resultSet.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

